    @Override
    public void keyPressed(KeyEvent ke) {
        String input;
        String mensaje;
        
        if(ke.getKeyCode()==KeyEvent.VK_ENTER){
            input=this.input.getText();
            this.addText("usted--->  "+input+"\n");
            this.input.setText("");
            mensaje=this.test.getMessage(input);
            this.addText("chatBot--->  "+mensaje+"\n");
            if(mensaje.equals("no entiendo")){
                this.lblAlert.setVisible(true);
                this.btnAdd.setVisible(true);
            }
            
        }
    }

